# Providing do_not as a variable
do_not = "don't"

# Providing y as a vaiable and the y contains the string
y = "Those who know #{binary} and those who #{do_not}"

# printing x
puts x
# printing y
puts y

# Printing with the help of x variable.
puts "I said: #{x}."
# Printing with the help of y variable.
puts "I also said: #{y}."

